// 学習ステップの落とし穴の型定義
interface Pitfall {
    title: string;
    detail: string;
}

// 練習問題の型定義
export interface Problem {
    question: string;
    answer: string;
    hint: string;
}

// 学習ステップの型定義
export interface LearningStep {
    title: string;
    explanation: string;
    pitfall: Pitfall;
    problems?: Problem[];
}

// 学習トピックの型定義
export interface LearningTopic {
    topic: string;
    steps: LearningStep[];
}

// 科目データの型定義
interface SubjectData {
    [key: string]: LearningTopic[];
}

// LaTeX変換用の正規表現置換関数
export function toLatexMath(str: string): string {
    if (!str) return str;
    let s = str;
    // 「ポイント：」の前後に改行を追加
    s = s.replace(/\n?\s*ポイント：/g, "\n\nポイント：\n");
    // 改行を<br>に変換
    s = s.replace(/\n/g, "<br>");

    // sqrt(2) → \sqrt{2}
    s = s.replace(/sqrt\(([^)]+)\)/g, "\\sqrt{$1}");
    // <=, >=
    s = s.replace(/<=/g, " \\le ");
    s = s.replace(/>=/g, " \\ge ");
    // pi → \pi
    s = s.replace(/\bpi\b/g, "\\pi");
    // 分数 例: 1/2 → \frac{1}{2}（ただし小数や式の一部でない場合のみ）
    s = s.replace(
        /(?<![\d.])([0-9]+)\s*\/\s*([0-9]+)(?![\d.])/g,
        "\\frac{$1}{$2}"
    );
    // 乗数 例: x^2 → x^{2}
    s = s.replace(/([a-zA-Z0-9]+)\^(-?[a-zA-Z0-9]+)/g, "$1^{$2}");

    return s;
}

// problemsのanswerをLaTeX記法に自動変換
function normalizeProblems(problems: Problem[]): Problem[] {
    return problems.map((p) => {
        let answer = p.answer;
        if (typeof answer === "string") {
            answer = toLatexMath(answer);
        }
        return { ...p, answer };
    });
}

